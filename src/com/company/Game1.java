        public void actionPerformed( ActionEvent e ) {
            if (e.getSource() == myTimer) {
                // begin to move player
                play.move();


                // move bullets
                for (int i = 0; i < shoot.size(); i++) {
                    shoot.get(i).move();


                }

                // check for player intersection with wall, reset player
                for (int j = 0; j < wall.size(); j++) {
                    if (play.intersects(wall .get(j))) {
                        resetPlayer();
                    }
                }
                // check for player intersection with Goomba, reset player
                    for (int j = 0; j < goomba.size(); j++) {
                        if (play.intersects(goomba.get(j))) {
                            resetPlayer();

                        }
                    }

                // check for bullet intersection with Goomba
                for (int k = 0; k < goomba.size(); k++){
                    for (int j = 0; j < shoot.size(); j++) {
                            if (shoot.get(j).intersects(goomba.get(k))){
                                System.out.println("Goomba Intersect");
                                // remove elements
                                goomba.remove(k);
                               shoot.remove(j);
                               goombaKilled++;
                            }
                        }

                    }



                if (e.getSource() == myTimer) {

                    // once player dies, reset game
                    if (play.health == 0){

                        JOptionPane.showMessageDialog( null, "GAME OVER!\nYou killed " + goombaKilled + " Goomba!\nYou reached level " + level + "!", "GAME OVER", JOptionPane.INFORMATION_MESSAGE );
                        System.out.println("GAME OVER");
                        flag = true;
                        counter = 0;
                        level = 1;
                        play.health = 30;
                        play.damage = 0;
                        playAgain();
                        resetGame();
                    }
                }

                // once player reaches finish line, load next map and reset game
                for (int i = 0; i < finish.size(); i++) {
                    if (play.intersects(finish.get(i))) {
                        counter++;
                        level++;
                        resetGame();

                    }


                }

                repaint();
            }



            else{

                // action buttons

                JButton b= (JButton)e.getSource();
                if (b.getText()=="Start"){
                    // begin game and timer
                    myTimer.start();

                    mainPanel.setVisible(true);
                    flag = false;
                }
                else if (b.getText()=="Game Rules"){

                    // display rules
                    JOptionPane.showMessageDialog( null, " 1. Try and reach the end of the maze by moving your player using the arrow keys!\n 2. Touching the wall or Goomba will cause you to restart!\n 3. Fire your bullets using the W A S D keys to eliminate the Goomba!", "Game Rules", JOptionPane.INFORMATION_MESSAGE );

                }
                else if (b.getText()=="Exit"){
                    // close program

                    System.exit(0);
                }
            }
        }//end of actionPerformed()




